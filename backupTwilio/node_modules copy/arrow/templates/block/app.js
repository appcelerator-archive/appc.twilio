var Arrow = require('arrow'),
	server = new Arrow(),
	Block = require('./lib'),
	block = new Block(Arrow, server);

// add a simple user model.
var User = Arrow.Model.extend('user', {
	fields: {
		name: { type: String, required: false, validator: /[a-zA-Z]{3,}/ }
	},
	connector: 'memory'
});
server.addModel(User);

// add our block to our server manually.
server.app.use(block.getMiddleware());

server.start();
