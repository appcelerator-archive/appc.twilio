var Arrow = require('arrow');

var User = Arrow.Model.extend('<%=name%>', {
	fields: {
		FirstName: { name: 'first_name', type: String, default: 'John' },
		LastName: { name: 'last_name', type: String, default: 'Smith' },
		Email: { name: 'email', type: String, required: true }
	},
	connector: 'memory'
});

module.exports = User;