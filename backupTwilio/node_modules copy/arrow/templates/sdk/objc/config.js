module.exports = {
	name: 'Objective-C',
	typeMap: {
		'string': 'NSString *',
		'date': 'NSDate *',
		'object': 'NSDictionary *',
		'array': 'NSArray *',
		'number': 'NSNumber *',
		'integer': 'NSNumber *'
	},
	cachePrefix: 'CacheType',
	standardArgsMap: {
		body: '(NSDictionary *)body',
		query: '(NSDictionary *)query',
		headers: '(NSDictionary *)headers'
	},
	pluckPathParams: function (params) {
		var retVal = [];
		for (var i = 0; i < params.length; i++) {
			var param = params[i];
			retVal.push('(' + (this.typeMap[param.dataType] || 'NSString *') + ')' + this.translatePathName(param.name));
		}
		return retVal;
	},
	translatePathName: function (name) {
		return name === 'id' ? 'ID' : name;
	}
};