module.exports = {
	name: 'Android',
	typeMap: {
		'string': 'String',
		'date': 'Date',
		'object': 'JSONObject',
		'array': 'JSONArray',
		'number': 'Number',
		'integer': 'Integer'
	},
	standardArgsMap: {
		body: 'JSONObject body',
		query: 'HashMap<String, Object> query',
		headers: 'HashMap<String, String> headers'
	},
	pluckPathParams: function (params) {
		var retVal = [];
		for (var i = 0; i < params.length; i++) {
			var param = params[i];
			retVal.push((this.typeMap[param.dataType] || 'String') + ' ' + param.name);
		}
		return retVal;
	}
};