var CLI = require('../cli'),
	_ = require('lodash'),
	util = require('./util'),
	New = require('../command/new');

// jscs:disable jsDoc

module.exports = {
	name: 'Empty Connector Project',
	type: 'arrow_connector_generator',
	generator: true,
	execute: generate,
	fields: [],
	order: 9999
};

function generate(appc, opts, callback) {
	var instance = new New(appc);
	instance.cli.program = opts;

	// remove default logger, which will be re-assigned to the one in CLI
	delete instance.cli.logger;
	_.defaults(instance.cli, CLI);

	instance.run(callback, 'connector');
}
