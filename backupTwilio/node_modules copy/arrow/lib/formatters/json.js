/**
 * Formats the body as JSON.
 * @param req
 * @param resp
 * @param body
 * @param singular
 * @param plural
 * @param callback
 */
exports.format = function jsonFormatter(req, resp, body, singular, plural, callback) {
	resp && resp.set('Content-Type', 'application/json');
	callback(null, JSON.stringify(body));
};

exports.defaultMimeType = 'application/json';
