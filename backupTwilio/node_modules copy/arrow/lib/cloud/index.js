/**
 * start the spinner
 */
function startSpinner() {
	if (appc) {
		appc.spinner.start();
	}
}

/**
 * stop the spinner
 */
function stopSpinner() {
	if (appc) {
		appc.spinner.stop();
	}
}

/**
 * write a wait message and start spinner
 */
function waitMessage(msg) {
	if (appc) {
		appc.log.info(msg);
	}
}

/**
 * write message and stop spinner
 */
