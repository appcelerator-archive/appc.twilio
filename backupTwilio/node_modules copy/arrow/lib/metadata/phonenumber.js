var _ = require('lodash');

exports = module.exports = Phonenumber;

/**
 * A phone number type.
 * @param obj
 * @returns {*}
 * @constructor
 */
function Phonenumber(obj) {
	return _.merge({
		type: 'text',
		subtype: 'phonenumber',
		required: false,
		default: '',
		validator: /^\+?\s?(\d+)?\s?[(]{0, 1}[0-9]{3}[)]{0, 1}[-\s\.]{0, 1}[0-9]{3}[-\s\.]{0, 1}[0-9]{4}$/
	}, obj);
}

