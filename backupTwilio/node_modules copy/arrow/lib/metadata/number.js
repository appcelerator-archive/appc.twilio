var _ = require('lodash');

exports = module.exports = NumField;

/**
 * A number type.
 * @param obj
 * @returns {*}
 * @constructor
 */
function NumField(obj) {
	return _.merge({
		type: 'text',
		subtype: 'number',
		required: false,
		default: 0,
		validator: /^-?\d*\.?\d+$/
	}, obj);
}
