var _ = require('lodash');

exports = module.exports = Radio;

/**
 * A radio type.
 * @param obj
 * @returns {*}
 * @constructor
 */
function Radio(obj) {
	return _.merge({
		type: 'radio',
		required: false,
		default: '',
		validator: null,
		selected: false
	}, obj);
}
