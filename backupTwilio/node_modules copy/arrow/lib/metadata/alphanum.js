var _ = require('lodash');

exports = module.exports = Alphanum;

/**
 * An alpha numerical type.
 * @param obj
 * @returns {*}
 * @constructor
 */
function Alphanum(obj) {
	return _.merge({
		type: 'text',
		subtype: 'alphanum',
		required: false,
		default: '',
		validator: /^[a-z0-9]+$/i
	}, obj);
}
