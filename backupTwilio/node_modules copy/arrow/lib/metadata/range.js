var _ = require('lodash');

exports = module.exports = RangeField;

/**
 * A range field type.
 * @param obj
 * @returns {*}
 * @constructor
 */
function RangeField(obj) {
	return _.merge({
		type: 'range',
		required: false,
		default: 0,
		validator: null,
		min: Number.MIN_VALUE,
		max: Number.MAX_VALUE
	}, obj);
}
