var props = [
	'new',
	'run',
	'publish',
	'unpublish',
	'arrow_generate_connector',
	'arrow_generate_model',
	'arrow_generate_block',
	'arrow_generate_blank_connector'
];

// jscs:disable jsDoc

// lazy load plugin modules
props.forEach(function (prop) {
	Object.defineProperty(exports, prop, {
		configurable: true,
		enumerable: true,
		get: function () {
			return require('./lib/plugin/' + prop);
		},
		set: function (v) {
			exports[prop] = v;
		}
	});
});

// special handling for generate plugin
Object.defineProperty(exports, 'generate', {
	configurable: true,
	enumerable: true,
	get: function () {
		return [
			require('./lib/plugin/generate'),
			require('./lib/plugin/generate_sdk')
		];
	},
	set: function (v) {
		exports.generate = v;
	}
});
