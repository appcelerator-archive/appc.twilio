/**
 * @class Arrow.Metadata
 * Collection of metadata objects used for field validation of the connector's
 * configuration object.
 *
 * The {@link Arrow.Connector#fetchMetadata} method should return an object with its `fields`
 * property set to an array of metadata objects used to validate the connector's configuration.
 *
 *     Arrow.Connector.extend({
 * 	       fetchMetadata: function (cb) {
 * 	           cb(null, {
 * 	               fields: [
 *                     Arrow.Metadata.Text({
 * 	                       name: 'username',
 *                         description: 'Username account',
 *                         default: 'admin',
 *                         required: true
 *                     }),
 *                     // more metadata objects...
 *                 ]
 *             });
 *         },
 *         // more connector methods...
 *     });
 */

/**
 * @class ArrowMetadataObject
 * Basic metadata object.
 * @pseudo
 */
/**
 * @property {Any} default
 * Default value for the field if undefined.
 * Default is an empty string or zero depending on the metadata.
 */
/**
 * @property {String} description
 * Human-readable description of the field.
 */
/**
 * @property {String} name
 * Field name.
 */
/**
 * @property {Boolean} required
 * Set to `true` if the field is required. Default is false.
 */
/**
 * @property {RegExp} validator
 * Regular expression used to validate the field value.
 */

/**
 * @class Arrow.Metadata.Alphanum
 * @extends ArrowMetadataObject
 * Metadata object used to evaluate an alphanumeric value.  By default, the validator property
 * is set to validate a string containing only letters and digits.
 */
/**
 * @constructor
 * Creates a new metadata object.
 * @param {Object} params Set any Metadata properties on the object.
 */

