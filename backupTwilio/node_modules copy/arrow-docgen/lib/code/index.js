var curl = require('./curl'),
	node = require('./node'),
	titanium = require('./titanium'),
	web = require('./web');

function generate(object, api, endpoint, opts) {
	var code = [];
	code.push(curl.generate(object, api, endpoint, opts));
	code.push(titanium.generate(object, api, endpoint, opts));
	code.push(node.generate(object, api, endpoint, opts));
	code.push(web.generate(object, api, endpoint, opts));
	return code.join('\n');
}

exports.generate = generate;
