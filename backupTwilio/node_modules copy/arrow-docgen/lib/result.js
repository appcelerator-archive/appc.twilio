function generate(object, api, endpoint, opts) {
	var code = [];
	if (endpoint.result) {
		if (Array.isArray(endpoint.result.example)) {
			code.push('> The above returns a JSON array like this:\n');
		} else if (typeof(endpoint.result.example) === 'object') {
			code.push('> The above returns a JSON object like this:\n');
		} else if (endpoint.result.example) {
			code.push('> The above returns data like this:\n');
		} else {
			code.push('> This method returns no data\n');
		}
		code.push('```' + endpoint.result.type);
		if (endpoint.result.type === 'json') {
			code.push(JSON.stringify(endpoint.result.example, null, 2));
		} else {
			code.push(endpoint.result.example);
		}
		code.push('```');
	}
	return code.join('\n');
}

exports.generate = generate;
