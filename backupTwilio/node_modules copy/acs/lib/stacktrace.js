var ACS_PATH = '<ACS_PATH>';
Error.prepareStackTrace = function customPrepareStackTrack(err, callsites) {
    var trace = err + '\n';
    var i;
    for(i in callsites) {
        if(callsites.hasOwnProperty(i)) {
            var c = callsites[i];
            if(c) {
                var filePath = c.getFileName();
                if(filePath) {
                    if(filePath.indexOf(ACS_PATH) === -1) {
                        trace += '    at ' + c + '\n';
                    }
                } else {
                    trace += '    at ' + c + '\n';
                }
            }
        }
    }
    return trace;
};