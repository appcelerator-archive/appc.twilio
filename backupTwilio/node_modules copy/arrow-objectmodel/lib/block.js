// jscs:disable jsDoc

const SPEC = '1';

function Block(arrow, block) {
	this.block = block;
}

Block.prototype.toJSON = function () {
	return {
		spec: SPEC,
		kind: 'block',
		documented: this.block.documented,
		name: this.block.name,
		filename: this.block.filename,
		description: this.block.description,
		metadata: this.block.metadata
	};
};

Block.prototype.inspect = function () {
	return JSON.stringify(this.toJSON(), null, '\t');
};

module.exports = Block;
