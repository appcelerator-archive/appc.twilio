// jscs:disable jsDoc

const SPEC = '1';

function Connector(arrow, connector) {
	this.connector = connector;
}

Connector.prototype.toJSON = function () {
	return {
		spec: SPEC,
		kind: 'connector',
		documented: this.connector.config.documented,
		name: this.connector.name,
		filename: this.connector.filename,
		description: this.connector.description,
		metadata: this.connector.metadata,
		schema: this.connector.schema,
		pkginfo: this.connector.pkginfo
	};
};

Connector.prototype.inspect = function () {
	return JSON.stringify(this.toJSON(), null, '\t');
};

module.exports = Connector;
